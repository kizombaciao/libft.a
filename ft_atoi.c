#include "libft.h"

int	ft_atoi(const char *s)
{
	int n;
	int sign;

	while (ft_isspace(*s++))
		;
	s--;
	sign = 1;
	if (*s == '-')
		sign = -1;
	if (*s == '+' || *s == '-')
		s++;
	n = 0;
	while (ft_isdigit(*s))
	{
		n = n * 10 + (*s - '0');
		s++;
	}
	return (sign * n);
}
