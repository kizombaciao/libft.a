#define SHIFT   'a' - 'A'

char	*ft_strupcase(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			str[i] -= SHIFT;
		}
		i++;
	}
	return (str);
}
