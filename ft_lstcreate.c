#include "libft.h"

t_list	*ft_lstcreate(size_t n)
{
	t_list *p1;
	t_list *p2;

	p1 = NULL;
	while (n--)
	{
		p2 = p1;
		p1 = ft_lstnew(NULL, 0);
		p1->next = p2;
	}
	return (p1);
}
